// --- Data Model for a Match Result ---
@HiveType(typeId: 0)
class MatchResult extends HiveObject {
  @HiveField(0)
  final String teamAName;

  @HiveField(1)
  final String teamBName;

  @HiveField(2)
  final int teamAScore;

  @HiveField(3)
  final int teamBScore;

  @HiveField(4)
